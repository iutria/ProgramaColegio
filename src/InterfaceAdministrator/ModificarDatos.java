    private void cargarDatos(){
        comboMes.setSelectedItem(editar.getMesNacimiento());
        comboDia.setSelectedItem(editar.getDiaNacimiento());
        textoDocumento.setText(documento);
        textoNombre.setText(editar.getNombre());
        if(tipo.equals("ESTUDIANTE")){
            textoMateria.setVisible(false);
            comboTipo.setSelectedItem(editar.getGrado());
        }else{
            textoMateria.setVisible(true);
            textoMateria.setText(editar.getMateria());
            comboTipo.setVisible(false);
        }
        textoDireccion.setText(editar.getDireccion());
        textoTelefono.setText(editar.getTelefono());
        comboSexo.setSelectedItem(editar.getSexo());
        comboYear.setSelectedItem(editar.getYearNacimiento());
    }
    private void modificar(){
        if(verificarCampos()==true){
            String nombre = textoNombre.getText().toUpperCase();
            String telefono = textoTelefono.getText();
            String direccion = textoDireccion.getText().toUpperCase();
            String sexo = comboSexo.getSelectedItem().toString();
            String year = comboYear.getSelectedItem().toString();
            String mes = comboMes.getSelectedItem().toString();
            String dia = comboDia.getSelectedItem().toString();
            String grado = comboTipo.getSelectedItem().toString();
            String materia = comboTipo.getSelectedItem().toString();
            if(tipo.equals("DOCENTE")){
                new ModificarDatosPersona().modificarDatosDocente(documento, nombre,telefono,direccion,sexo,year,mes,dia,materia);
                getToolkit().beep();
                JOptionPane.showMessageDialog(rootPane, "SE HAN CAMBIADO LOS DATOS");
                terminar();
            }else{
                new ModificarDatosPersona().modificarDatosEstudiante(documento, nombre,telefono,direccion,sexo,year,mes,dia,grado);
                getToolkit().beep();
                JOptionPane.showMessageDialog(rootPane, "SE HAN CAMBIADO LOS DATOS");
                terminar();
            }   

        }else{
            JOptionPane.showMessageDialog(rootPane, "VERIFIQUE QUE LOS DATOS ESTEN CORRECTOS");
        }
    }
    private boolean verificarCampos(){
        if(tipo.equals("ESTUDIANTE")){
            if(textoNombre.getText().trim().equals("") 
            || textoTelefono.getText().trim().equals("")
            || textoDireccion.getText().trim().equals("")
            || comboSexo.getSelectedItem().toString().trim().equals("SELECCIONE")
            || comboTipo.getSelectedItem().toString().trim().equals("SELECCIONE")
            || comboDia.getSelectedItem().toString().trim().equals("DD")
            || comboMes.getSelectedItem().toString().trim().equals("MM")
            || comboYear.getSelectedItem().toString().trim().equals("AAAA")){
                return false;
            }
            else{
                return true;
            }
        }else{
            if(textoNombre.getText().trim().equals("") 
            || textoTelefono.getText().trim().equals("")
            || textoDireccion.getText().trim().equals("")
            || comboSexo.getSelectedItem().toString().trim().equals("SELECCIONE")
            || comboDia.getSelectedItem().toString().trim().equals("DD")
            || comboMes.getSelectedItem().toString().trim().equals("MM")
            || comboYear.getSelectedItem().toString().trim().equals("AAAA")){
                return false;
            }
            else{
                return true;
            }
        }
    }
    
    private void limpiar(){
        textoNombre.setText("");
        textoDocumento.setText("");
        textoTelefono.setText("");
        textoDireccion.setText("");
        comboTipo.setSelectedItem("SELECCIONE");
        comboDia.setSelectedItem("DD");
        comboMes.setSelectedItem("MM");
        comboYear.setSelectedItem("AAAA");
        comboSexo.setSelectedItem("SELECCIONE");
    }
    
    private void llenarCombos(){
        comboYear.addItem("AAAA");
        comboDia.addItem("DD");
        
        for(int i = 2018; i>=1938;i--){
            comboYear.addItem(""+i);
        }
        for(int i = 1; i<=31;i++){
            comboDia.addItem(""+i);
        }
    }
    private void llenarComboTipoDocente(){
        comboTipo.addItem("ESPAÑOL"); 
        comboTipo.addItem("MATEMATICAS");
        comboTipo.addItem("SOCIALES");
        comboTipo.addItem("INFORMATICA");
        comboTipo.addItem("NATURALES");
        comboTipo.addItem("ARTISTICA");
        comboTipo.addItem("DEPORTE");
    }
    private void llenarComboTipoEstudiante(){
        for (int i = 6; i <= 11; i++) {
            comboTipo.addItem(""+i);
        }
    }
    private void terminar(){
        
        new MostrarPersona(tipo).setVisible(true);
        this.dispose();
    }
    
