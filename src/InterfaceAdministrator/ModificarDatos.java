    public ModificarDatos(String tipo, String documento) {
        initComponents();
        
        editar = new EditarDatos();
        
        if(tipo.equals("DOCENTE")){
            editar.buscarDocente(documento);
            textoTipo.setText("ASIGNATURA:");
            textoTitulo.setText("MODIFICAR DATOS DE DOCENTE");
            textoUrl.setText("INICIO / REGISTRAR PERSONA / REGISTRAR DOCENTE / DOCENTES REGISTRADOS / MODIFICAR DATOS");
            llenarComboTipoDocente();
        }
        else{
            editar.buscarEstudiante(documento);
            textoTitulo.setText("MODIFICAR DATOS DE ESTUDIANTE");
            textoUrl.setText("INICIO / REGISTRAR PERSONA / REGISTRAR ESTUDIANTES / ESTUDIANTES REGISTRADOS / MODIFICAR DATOS");
            textoTipo.setText("GRADO:");
            llenarComboTipoEstudiante();
        }
        
        this.tipo = tipo;
        this.documento = documento;
        llenarCombos();
        panel=false;
        cargarDatos();
    }

