    private void editarNota(){
        try{
            new EditarNotas(tabla.getValueAt(tabla.getSelectedRow(), 0).toString(), documentoDocente).setVisible(true);
            this.dispose();
        }catch(Exception e){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "DEBE SELECCIONAR EL ESTUDIANTE PARA PODER EDITAR LAS NOTAS","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }
    
