    private void guardar(){
        if(verificarTextoNotas()){
            if(verificarNotas()){
                if(cambiarNotas.cambiarNotas(""+notaUno, 
                ""+notaDos, ""+notaTres, 
                textoObservaciones.getText(), documento, documentoDocente)){
                    getToolkit().beep();
                    JOptionPane.showMessageDialog(rootPane, "NOTAS CAMBIADAS","EXITO",JOptionPane.INFORMATION_MESSAGE);
                    new MostrarNotasRegistradas(documentoDocente).setVisible(true);
                    this.dispose();
                }
            }else{
                getToolkit().beep();
                JOptionPane.showMessageDialog(rootPane, "VERIFIQUE QUE LAS NOTAS INGRESADAS SON CORRECTAS","NOTA INVALIDA",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "DEBE DILIGENCIAR TODAS LAS CASILLAS","INVALIDO",JOptionPane.ERROR_MESSAGE);
        }
    }
    private void llenar(){
        textoNombre.setText(new BuscarPersona().buscarNombreEstudiante(documento));
        textoNotaUno.setText(cambiarNotas.getNotaUno());
        textoNotaDos.setText(cambiarNotas.getNotaDos());
        textoNotaTres.setText(cambiarNotas.getNotaTres());
        textoObservaciones.setText(cambiarNotas.getObservaciones());
    }
    private boolean verificarNotas(){
        
        try{
            notaUno = Float.parseFloat(textoNotaUno.getText());
            notaDos = Float.parseFloat(textoNotaDos.getText());
            notaTres = Float.parseFloat(textoNotaTres.getText());
            if(notaUno>5 || notaDos>5 || notaTres>5)
            {
                return false;
            }
        }catch(Exception e){
            return false;
        }
        return true;
    }
    private boolean verificarTextoNotas(){
        if(textoNotaUno.getText().equals("") 
        || textoNotaDos.getText().equals("") 
        || textoNotaTres.getText().equals("")
        || textoObservaciones.getText().equals(""))
        {
            return false;
        }
        return true;
    }
    private void limpiar(){
        textoNotaUno.setText("");
        textoNotaDos.setText("");
        textoNotaTres.setText("");
        textoObservaciones.setText("");
    }
